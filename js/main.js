import Ghost from './ghost.js';
import Player from './player.js';

const canvas = document.getElementById('game');
const context = canvas.getContext('2d');

const sprites = new Image();
sprites.src = 'assets/image.png';

const blinky = new Ghost(context,sprites,'blinky');
const pinky = new Ghost(context,sprites,'pinky');
const inky = new Ghost(context,sprites,'inky');
const clyde = new Ghost(context,sprites,'clyde');

const player = new Player(context,sprites);

pinky.position = {x: 26, y: 4};
inky.position = {x: 46, y: 4};
clyde.position = {x: 66, y: 4};

sprites.onload = update();

function update(){
    blinky.update();
    pinky.update();
    inky.update();
    clyde.update();

    player.update();

    drawMap();

    blinky.show();
    pinky.show();
    inky.show();
    clyde.show();

    player.show();

    requestAnimationFrame(update);
}

function drawMap(){
    const mapWidth = 227;
    const mapHeight = 250;
    context.drawImage(sprites,226,0,mapWidth,mapHeight,0,0,227,248);
}


